class MethodsHelper{
  static String addPromotion(String promotion){
    switch (promotion){
      case ('L0') :
        return 'LO';
      case ('L1') :
        return 'L1';
      case ('L2') :
        return 'L2';
      case ('L3') :
        return 'L3';
      case ('M1') :
        return 'M1';
      case ('M2') :
        return 'M2';
      default:
        return '';
    }
  }
}



